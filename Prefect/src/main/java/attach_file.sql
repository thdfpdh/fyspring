DROP INDEX PK_ATTACH_FILE;

/* 첨부파일 */
DROP TABLE ATTACH_FILE 
    CASCADE CONSTRAINTS;

/* 첨부파일 */
CREATE TABLE ATTACH_FILE (
    UUID VARCHAR2(45) NOT NULL, /* UUID */
    SEQ NUMBER(2) NOT NULL, /* 순번 */
    ORG_FILE_NAME VARCHAR2(300), /* 원본파일명 */
    SAVE_FILE_NAME VARCHAR2(50), /* 저장파일명 */
    EXTENSION VARCHAR2(5), /* 확장자 */
    FILE_SIZE NUMBER(8), /* 사이즈 */
    SAVE_PATH VARCHAR2(80), /* 저장경로 */
    REG_DT DATE DEFAULT SYSDATE, /* 등록일 */
    REG_ID VARCHAR2(30) /* 등록자 */
);

COMMENT ON TABLE ATTACH_FILE IS '첨부파일';

COMMENT ON COLUMN ATTACH_FILE.UUID IS 'UUID';

COMMENT ON COLUMN ATTACH_FILE.SEQ IS '순번';

COMMENT ON COLUMN ATTACH_FILE.ORG_FILE_NAME IS '원본파일명';

COMMENT ON COLUMN ATTACH_FILE.SAVE_FILE_NAME IS '저장파일명';

COMMENT ON COLUMN ATTACH_FILE.EXTENSION IS '확장자';

COMMENT ON COLUMN ATTACH_FILE.FILE_SIZE IS '사이즈';

COMMENT ON COLUMN ATTACH_FILE.SAVE_PATH IS '저장경로';

COMMENT ON COLUMN ATTACH_FILE.REG_DT IS '등록일';

COMMENT ON COLUMN ATTACH_FILE.REG_ID IS '등록자';

CREATE UNIQUE INDEX PK_ATTACH_FILE
    ON ATTACH_FILE (
        UUID ASC,
        SEQ ASC
    );

ALTER TABLE ATTACH_FILE
    ADD
        CONSTRAINT PK_ATTACH_FILE
        PRIMARY KEY (
            UUID,
            SEQ
        );